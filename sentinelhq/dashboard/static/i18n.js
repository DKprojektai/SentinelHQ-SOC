var _lang = localStorage.getItem('shq_lang') || 'lt';

function t(key, vars) {
  const val = (TRANSLATIONS[_lang] || TRANSLATIONS['lt'])[key] || key;
  if (!vars) return val;
  return val.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? '');
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('shq_lang', lang);
  document.getElementById('html-root').lang = lang;
  applyTranslations();
  fetch('/api/set-lang', {method:'POST', credentials:'include', headers:{'Content-Type':'application/json'}, body:JSON.stringify({lang})}).catch(()=>{});
  if (typeof refreshAll === 'function') refreshAll();
  if (typeof loadCands === 'function') loadCands();
  if (typeof loadTopCands === 'function') loadTopCands();
  if (typeof window.renderPlaybooksFiltered === 'function') window.renderPlaybooksFiltered();
  document.dispatchEvent(new CustomEvent('langChanged', {detail: {lang}}));
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.getAttribute('data-i18n'));
    if (el.tagName === 'OPTION') el.textContent = val;
    else el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === _lang);
  });
}

document.addEventListener('DOMContentLoaded', function() {
  applyTranslations();
  fetch('/api/set-lang', {method:'POST', credentials:'include', headers:{'Content-Type':'application/json'}, body:JSON.stringify({lang: _lang})}).catch(()=>{});
});
